/**
 * Класс выполняющий математические действия
 */
public class MathOperations {


    double summa(double a, double b) {
        return a + b;
    }

    double raznost(double a, double b) {
        return a - b;
    }

    double chastnoe(double a, double b) {
        return a / b;
    }

    double proizvedenie(double a, double b) {
        return a * b;
    }
}
